def test_parse_input():
    grid, robot = parse_input(DATA)
    assert grid.w == 10
    assert grid.h == 10
    assert grid.start_y == 4
    assert grid.start_x == 4
    assert sum(row.count(BOX) for row in grid.grid) == 21
    assert sum(row.count(OBSTACLE) for row in grid.grid) == 37
    assert sum(row.count(ROBOT) for row in grid.grid) == 1
    assert len(robot.directions) == 700


def test_move():
    grid, robot = parse_input(DATA)
    grid.run_robot(robot)
    assert robot.x == 3
    assert robot.y == 4


